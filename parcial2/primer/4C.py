# P4C_imagen.py — Mostrar logo como array de bytes
# El array se genera con image2cpp (javl.github.io/image2cpp)
# Configuración: 32x32 px, 'Plain bytes', 'Horizontal — 1 bit per pixel'
from machine import I2C, Pin
import ssd1306
import framebuf
i2c = I2C(0, scl=Pin(22), sda=Pin(21), freq=400000)
oled = ssd1306.SSD1306_I2C(128, 64, i2c)
# ── Array de ejemplo: logo 'T' de TESOEM (32x32) ──────────────────
# Para generar el tuyo: convertir imagen PNG a BMP 1-bit, luego image2cpp
logo_bytes = bytearray([
 0xFF,0xFF,0xFF,0xFF, # fila 0 — barra horizontal superior
 0xFF,0xFF,0xFF,0xFF, # fila 1
 0xFF,0xFF,0xFF,0xFF, # fila 2
 0xFF,0xFF,0xFF,0xFF, # fila 3
 0x07,0xE0,0x07,0xE0, # fila 4 — inicio del tallo de la T
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
 0x07,0xE0,0x07,0xE0,
])
# Crear FrameBuffer temporal con la imagen
logo_fb = framebuf.FrameBuffer(logo_bytes, 32, 32, framebuf.MONO_HLSB)
# Mostrar logo centrado
oled.fill(0)
oled.blit(logo_fb, 48, 16) # centrar: x=(128-32)/2=48, y=(64-32)/2=16
oled.show()
